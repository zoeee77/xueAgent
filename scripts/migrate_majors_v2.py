def migrate_majors_v1_to_v2(input_path: Path, output_path: Path):
    """执行 v1 -> v2 数据迁移。"""
    with open(input_path, "r", encoding="utf-8") as f:
        majors_v1 = json.load(f)

    majors_v2 = {}
    migrated = 0
    skipped = 0

    for name, data in majors_v1.items():
        if name in MAJOR_EXTENSIONS:
            # 保留原有字段
            majors_v2[name] = dict(data)
            # 添加新字段
            majors_v2[name].update(MAJOR_EXTENSIONS[name])
            migrated += 1
        else:
            # 未知专业，保留原数据但添加空扩展字段
            majors_v2[name] = dict(data)
            majors_v2[name].setdefault("courses", [])
            majors_v2[name].setdefault("skills_required", [])
            majors_v2[name].setdefault("personality_fit", [])
            majors_v2[name].setdefault("career_paths", [])
            majors_v2[name].setdefault("industries", [])
            majors_v2[name].setdefault("keywords", [name])
            skipped += 1

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(majors_v2, f, ensure_ascii=False, indent=2, sort_keys=True)

    print(f"迁移完成: {migrated} 个专业已补充扩展字段, {skipped} 个专业使用默认扩展")
    return majors_v2


